/**
 * マークダウン記号を除去して plain text にする
 *
 * TOMO の応答に `**`, `##`, `\`code\``, `- list` などが混入した場合の
 * クライアント側フォールバック。システムプロンプトでも禁止しているが、
 * 100% 守られない場合があるので二重防御。
 */
export function stripMarkdown(text: string): string {
  if (!text) return text;
  return (
    text
      // 太字 / 強調
      .replace(/\*\*([^*\n]+)\*\*/g, "$1")
      .replace(/__([^_\n]+)__/g, "$1")
      .replace(/(?<!\*)\*([^*\n]+)\*(?!\*)/g, "$1")
      .replace(/(?<!_)_([^_\n]+)_(?!_)/g, "$1")
      // 見出し (# / ## / ### 等)
      .replace(/^#{1,6}\s+/gm, "")
      // インラインコード
      .replace(/`([^`\n]+)`/g, "$1")
      // コードブロック (```...```)
      .replace(/```[\w-]*\n?/g, "")
      // リンク [text](url) → text
      .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1")
      // 画像 ![alt](url) → alt
      .replace(/!\[([^\]]*)\]\([^)]+\)/g, "$1")
      // 取り消し線
      .replace(/~~([^~\n]+)~~/g, "$1")
      // 箇条書きマーカー (- / * / +) → ・
      .replace(/^[\s]*[-*+]\s+/gm, "・")
      // 引用ブロック >
      .replace(/^>\s*/gm, "")
      // 水平線
      .replace(/^[-*_]{3,}$/gm, "")
  );
}
